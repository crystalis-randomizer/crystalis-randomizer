          ;; use a lookup table for this to speed it up?
          asl
          rol $13
          asl
          rol $13
          asl
          rol $13
          asl
          rol $13
          sta $12
          lda $0480,x           ; ObjectTimer,x
          and #$0f
          pha                   ; Stash timer in $30 for later
            tay
            ;; Index into the VelocityComponents table
            lda ($12),y
            sta $30
            ;; Do it again for the other component
            lda #$0a
            sta $13
            lda $31
            asl
            rol $13
            asl
            rol $13
            asl
            rol $13
            asl
            rol $13
            sta $12
          pla
          tay
          ;; Index the second time
